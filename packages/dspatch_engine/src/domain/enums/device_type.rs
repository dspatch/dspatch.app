use serde::{Deserialize, Serialize};

/// Broad category of a registered device.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Serialize, Deserialize)]
#[serde(rename_all = "snake_case")]
pub enum DeviceType {
    /// macOS, Windows, or Linux.
    Desktop,

    /// iOS or Android.
    Mobile,
}
